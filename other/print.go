package other
import "fmt"
// PRINT WITH SINGLE LINE FUNCTION
func Print(content string){
	fmt.Print(content)
}
// PRINT WITH SINGLE LINE FUNCTION
func PrintWithNewLine(content string){
	fmt.Println(content)
}
// PRINT WITH VARIBALES 
func PrintWithVariables(content string , value any){
	fmt.Printf(content,value)
}

